// Определить, является ли число a делителем числа b ? И наоборот. (Дать два ответа)


let a = parseInt(prompt("Введіть ваше перше число:"));
let b = parseInt(prompt("Введіть ваше друге число:"));


if (b % a === 0) {
    alert(`${a} є дільником числа ${b}`);
} else {
    alert(`${a} не є дільником числа ${b}`);
}

if (a % b === 0) {
    alert(`${b} є дільником числа ${a}`);
} else {
    alert(`${b} не є дільником числа ${a}`);
}