// Дано число. Определить, заканчивается оно четной цифрой или нечетной? Вывести последнюю цифру.


let a = parseInt(prompt("Введіть ваше  число:"));

let lastDigit = a % 10;


if (a % 2 === 0) {
    alert(`${a} є парним числом. Остання цифра цього числа є: ${lastDigit}`);
} else {
    alert(`${a} є непарним числом. Остання цифра цього числа є: ${lastDigit}`);
}